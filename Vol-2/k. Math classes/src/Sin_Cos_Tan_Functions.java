public class Sin_Cos_Tan_Functions {
    public static void main(String[] args) {

        // Når disse funktioner bruges skal værdier passes i radianer


        /* -------- Sin ---------- */

        System.out.println(Math.sin(Math.PI));


        /* -------- Cos ---------- */

        System.out.println(Math.cos(Math.PI));



        /* -------- Tan ---------- */

        System.out.println(Math.tan(Math.PI));



        // Her konverteres 45 grader til Radianer så der kan tages Cos

        System.out.println(Math.cos(Math.toRadians(45)));




    }
}
