public class Invers_Cos_Sin_Tan {
    public static void main(String[] args) {


        /* -------- Sin ---------- */

        // Her beregnes radian 0.8 til sin til vinkel
        System.out.println(Math.toDegrees(Math.asin(0.8)));



        /* -------- Cos ---------- */

        // Her beregnes radian 0.8 til cos til vinkel
        System.out.println(Math.toDegrees(Math.acos(0.8)));



        /* -------- Tan ---------- */

        // Her beregnes radian 0.8 til tan til vinkel
        System.out.println(Math.toDegrees(Math.atan(0.8)));



    }
}
