public class Boolean {

    public static void main(String[] args) {

        /*

        Boolean datatype KUN kan være TRUE eller FALSE

        */


        // declare og assigne en boolean variabel
        boolean var1 = true;

        // printe værdien af boolean værdien
        if (var1) {
            System.out.println("The value is: " + var1);
        }

        // boolean variabel kan declares og assignes på mange måder
        boolean var2 = (15 > 7);

        if (var2)
        {
            System.out.println("The value is: " + var2);
        }





    }
}
