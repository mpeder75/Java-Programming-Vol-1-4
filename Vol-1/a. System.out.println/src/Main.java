public class Main {
    public static void main(String[] args) {

        // Insert new line after \n
        System.out.println("This will create a new line,\nthis is the NEW line");

        System.out.println("Countdown:\n5\n4\n3\n2\n1");


        // Insert TAB after \t
        System.out.println("Do you like me?\t\tYes\t\tNo");


        // Example
        System.out.println("Home Town Burger Restaurant");
        System.out.println("Hours:\nMon - Fri, 9am - 9pm");
        System.out.println("\nLowest prices in town");
        System.out.println("Lunch\tDinner");
        System.out.println("$5.95\t$11.95");

    }
}